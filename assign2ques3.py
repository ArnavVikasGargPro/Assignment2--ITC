'''ques 3 -For a=56 and b=10 with the help of bitwise operators calculate the following:
a. a&b
b. a|b
c. a^b
d. Left shift both a and b with 2 bits.
e. Right shift a with 2 bits and b with 4 bits.'''
a = 56
b = 10 
# AND 
print("a & b =", a & b)
#  OR 
print("a | b =", a | b)
#XOR 
print("a ^ b =", a ^ b)
# left shift operator
print("a << 2 =", a << 2)
print("b << 2 =", b << 2)
# right shift operator
print("a >> 2 =", a >> 2)
print("b >> 4 =", b >> 4)
